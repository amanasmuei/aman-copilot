function buildPromptFile(meta) {
  // YAML double-quoted strings: escape backslashes and double quotes.
  const desc = meta.description.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
  return [
    "---",
    `description: "${desc}"`,
    'mode: "agent"',
    "---",
    "",
    meta.body,
    "",
  ].join("\n");
}

async function main() {
  const corePath = await resolveLayer(".acore", "core.md");
  const rulesPath = await resolveLayer(".arules", "rules.md");
  const core = await readIfExists(corePath);
  const rules = await readIfExists(rulesPath);

  if (!core && !rules) {
    console.error(
      "No aman ecosystem found. Run: npx @aman_asmuei/aman@latest",
    );
    process.exit(1);
  }

  const amemInstalled = await exists(path.join(HOME, ".amem"));

  const content = buildInstructions({ core, rules, amemInstalled });

  const baseDir = userLevel
    ? path.join(HOME, ".config", "github-copilot")
    : path.join(process.cwd(), ".github");

  const instructionsPath = userLevel
    ? path.join(baseDir, "copilot-instructions.md")
    : path.join(baseDir, "copilot-instructions.md");
  const promptsDir = path.join(baseDir, "prompts");

  await fs.mkdir(baseDir, { recursive: true });
  await fs.writeFile(instructionsPath, content, "utf-8");

  // Prompt files (slash commands)
  await fs.mkdir(promptsDir, { recursive: true });
  const written = [];
  for (const [name, meta] of Object.entries(PROMPT_FILES)) {
    const p = path.join(promptsDir, name);
    await fs.writeFile(p, buildPromptFile(meta), "utf-8");
    written.push(name);
  }

  console.log("");
  console.log(`✓ Wrote ${instructionsPath}`);
  console.log(`✓ Wrote ${written.length} prompt files in ${promptsDir}`);
  for (const name of written) {
    const cmd = name.replace(".prompt.md", "");
    console.log(`    /${cmd}`);
  }
  console.log("");
  console.log("Layers included:");
  if (core) console.log(`  acore   ${corePath}`);
  if (rules) console.log(`  arules  ${rulesPath}`);
  if (amemInstalled) console.log("  amem    (guidance injected)");
  console.log("");
  console.log("Next:");
  console.log("  1. aman-copilot install-mcp   # register live MCP tools");
  console.log("  2. Restart VS Code");
  console.log("  3. Open Copilot Chat → Agent mode");
  console.log('  4. Try: "/identity what do you know about me?"');
  console.log("");
}

main().catch((err) => {
  console.error(`Failed: ${err instanceof Error ? err.message : String(err)}`);
  process.exit(1);
});
