#!/usr/bin/env node
/**
 * init.mjs — Write .github/copilot-instructions.md from the aman ecosystem.
 *
 * Reads scope-aware acore + arules files (engine v1), falls back to legacy
 * single-tenant paths, and renders a single instructions file that GitHub
 * Copilot Chat will auto-load into every chat turn in VS Code.
 *
 * Default target: ./.github/copilot-instructions.md (project-level).
 * --user flag:    ~/.config/github-copilot/intellij/copilot-instructions.md
 *                 (best-effort user-level; VS Code prefers project-level)
 *
 * Scope: dev:copilot
 */

import { promises as fs } from "node:fs";
import * as path from "node:path";
import * as os from "node:os";

const SCOPE_DIR = "dev/copilot";
const HOME = os.homedir();

const args = process.argv.slice(2);
const userLevel = args.includes("--user");

/**
 * Resolve a layer file by trying the new scope-aware path first (under
 * dev/copilot), then the shared dev/plugin scope (so users who already ran
 * aman-plugin don't have to duplicate config), then the legacy path.
 */
async function resolveLayer(layerDir, filename) {
  const candidates = [
    path.join(HOME, layerDir, SCOPE_DIR, filename),
    path.join(HOME, layerDir, "dev", "plugin", filename),
    path.join(HOME, layerDir, filename),
  ];
  for (const p of candidates) {
    try {
      await fs.access(p);
      return p;
    } catch {}
  }
  return null;
}

async function readIfExists(p) {
  if (!p) return null;
  try {
    return await fs.readFile(p, "utf-8");
  } catch {
    return null;
  }
}

async function main() {
  const corePath = await resolveLayer(".acore", "core.md");
  const rulesPath = await resolveLayer(".arules", "rules.md");
  const core = await readIfExists(corePath);
  const rules = await readIfExists(rulesPath);

  if (!core && !rules) {
    console.error(
      "No aman ecosystem found. Run: npx @aman_asmuei/aman@latest",
    );
    process.exit(1);
  }

  const amemInstalled = await fs
    .access(path.join(HOME, ".amem"))
    .then(() => true)
    .catch(() => false);

  const sections = [
    "<!-- Generated by aman-copilot. Do not edit by hand — re-run `aman-copilot init`. -->",
    "# aman — AI companion configuration for GitHub Copilot Chat",
    "",
    "The following is your AI companion configuration. Read it and behave accordingly.",
    "Scope: `dev:copilot`.",
    "",
  ];

  if (core) {
    sections.push("## Identity (acore)", "", core.trim(), "");
  }
  if (rules) {
    sections.push("---", "", "## Guardrails (arules)", "", rules.trim(), "");
  }
  if (amemInstalled) {
    sections.push(
      "---",
      "",
      "## Persistent memory (amem)",
      "",
      "You have access to the `amem-memory` MCP server. Use it proactively:",
      "",
      "- On new tasks: call `memory_inject` with the task topic to surface corrections and decisions.",
      "- On corrections (\"don't\", \"never\", \"stop\"): call `memory_store` with type=correction, confidence=1.0.",
      "- On architecture decisions: `memory_store` with type=decision.",
      "- For recall: `memory_recall` (compact) then `memory_detail` for full content.",
      "",
      "Corrections are absolute constraints. They override everything else.",
      "",
    );
  }
  sections.push(
    "---",
    "",
    "## Live tools (aman-mcp)",
    "",
    "The `aman` MCP server exposes identity_read, rules_check, rules_add, eval_log and ~28 more tools. Prefer calling these over guessing — they're the source of truth for the user's configuration.",
    "",
  );

  const content = sections.join("\n");

  const target = userLevel
    ? path.join(HOME, ".config", "github-copilot", "copilot-instructions.md")
    : path.join(process.cwd(), ".github", "copilot-instructions.md");

  await fs.mkdir(path.dirname(target), { recursive: true });
  await fs.writeFile(target, content, "utf-8");

  console.log("");
  console.log(`✓ Wrote ${target}`);
  console.log("");
  console.log("Layers included:");
  if (core) console.log(`  acore   ${corePath}`);
  if (rules) console.log(`  arules  ${rulesPath}`);
  if (amemInstalled) console.log("  amem    (guidance injected)");
  console.log("");
  console.log("Next:");
  console.log("  1. aman-copilot install-mcp   # register live MCP tools in VS Code");
  console.log("  2. Restart VS Code");
  console.log('  3. Ask Copilot Chat: "what do you know about me?"');
  console.log("");
}

main().catch((err) => {
  console.error(`Failed: ${err instanceof Error ? err.message : String(err)}`);
  process.exit(1);
});
