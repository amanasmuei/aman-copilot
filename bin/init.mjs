#!/usr/bin/env node
/**
 * init.mjs — Write copilot-instructions.md + prompt files from the aman
 * ecosystem for use in GitHub Copilot Chat.
 *
 * Outputs (relative to cwd, unless --user):
 *   .github/copilot-instructions.md   — loaded into every chat turn
 *   .github/prompts/identity.prompt.md  — /identity slash command
 *   .github/prompts/rules.prompt.md     — /rules slash command
 *   .github/prompts/eval.prompt.md      — /eval slash command
 *   .github/prompts/remember.prompt.md  — /remember slash command
 *
 * Scope: dev:copilot. Layer resolution cascades through:
 *   ~/.acore/dev/copilot/core.md
 *   ~/.acore/dev/plugin/core.md   (inherit from aman-plugin)
 *   ~/.acore/core.md              (legacy)
 */

import { promises as fs } from "node:fs";
import * as path from "node:path";
import * as os from "node:os";

const SCOPE_DIR = "dev/copilot";
const HOME = os.homedir();

const args = process.argv.slice(2);
const userLevel = args.includes("--user");

async function resolveLayer(layerDir, filename) {
  const candidates = [
    path.join(HOME, layerDir, SCOPE_DIR, filename),
    path.join(HOME, layerDir, "dev", "plugin", filename),
    path.join(HOME, layerDir, filename),
  ];
  for (const p of candidates) {
    try {
      await fs.access(p);
      return p;
    } catch {}
  }
  return null;
}

async function readIfExists(p) {
  if (!p) return null;
  try {
    return await fs.readFile(p, "utf-8");
  } catch {
    return null;
  }
}

async function exists(p) {
  try {
    await fs.access(p);
    return true;
  } catch {
    return false;
  }
}

